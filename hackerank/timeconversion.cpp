#include <stdio.h>

int main() {
    char s[256];    
    scanf("%s", s);

    if (s[0] == '1' &&
        s[1] == '2' &&
        s[8] == 'A' &&
        s[9] == 'M') {
        
        s[0] = '0';
        s[1] = '0';

    }
    else if (s[0] == '1' &&
        s[1] == '2' &&
        s[8] == 'P' &&
        s[9] == 'M') {

        // do nothing        
        
    } else if (s[8] == 'P' && s[9] == 'M') {
        int hh = (s[0] - '0')*10 + (s[1] - '0');
        hh += 12;
        s[0] = (hh/10) + '0';
        s[1] = (hh%10) + '0';
    }    
    s[8] = '\0';
    printf("%s\n", s);       
    
    return 0;
}
