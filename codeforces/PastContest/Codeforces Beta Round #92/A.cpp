int main(int argc, char *argv[]) {
   int n, a, b, res = 0;
   cin >> n >> a >> b;
   for(int i = 1; i <= n; ++i) {
      int behind = i - 1;
      int front = n - i;
      if(front >= a && behind <= b) res++;
   }
   cout << res << endl;
   return 0;
}

